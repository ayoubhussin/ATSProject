package main.AdminPage;

public class Blank {

    private String blankNumber;
    private String advisorID;

    public Blank(String a, String b) {
        blankNumber = a;
        advisorID = b;


    }

    public String getBlankNumber() {
        return blankNumber;
    }

    public String getAdvisorID() {
        return advisorID;
    }


}
